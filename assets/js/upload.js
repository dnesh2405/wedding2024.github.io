function addNewSection() {
        // Create a new section element
        var newSection = document.createElement("section");
        newSection.className = "col-6 col-12-narrower feature";

        // Create the inner content for the new section
        var content = `
            <div class="image-wrapper">
                <a href="images/pic02.jpg" class="image"><img src="images/pic02.jpg" alt="" /></a>
                <h2>NEW NAME</h2>
                <p>NEW COMMENT OR WISHES.</p>
            </div>
        `;

        // Set the innerHTML of the new section
        newSection.innerHTML = content;

        // Append the new section to the features container
        document.getElementById("features-container").appendChild(newSection);
    }

